public class TestOpr {
    public static void main(String[] args){
//        算數運算 + - * / %
        int a = 10, b = 20;
        System.out.println("a+b="+a+b); //1020
        System.out.println("a+b="+(a+b)); //30
        System.out.println("a-b="+(a-b)); //10
        System.out.println("a*b="+(b*a)); //10
        System.out.println("a/b="+(b/a)); //10
        System.out.println("a%b="+(a%b)); //10
    }
}
